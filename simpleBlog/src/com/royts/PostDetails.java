package com.royts;

public class PostDetails {
	String postTitle;
	String postId;
	public PostDetails(String postTitle, String postId) {
		this.postTitle = postTitle;
		this.postId = postId;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public String getPostId() {
		return postId;
	}
	
	
}
